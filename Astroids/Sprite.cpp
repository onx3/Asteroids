#include "Sprite.h"
#include "cassert"

Sprite::Sprite()
{
}

//------------------------------------------------------------------------------------------------------------------------

Sprite::~Sprite()
{
}

//------------------------------------------------------------------------------------------------------------------------

void Sprite::SetSprite(const std::string & file)
{
    if (!mTexture.loadFromFile(file))
    {
        assert(false && "Failed to load texture");
    }
    mSprite.setTexture(mTexture);
}

//------------------------------------------------------------------------------------------------------------------------

sf::Sprite Sprite::GetSprite()
{
    return mSprite;
}

