#include "GameComponent.h"

GameComponent::GameComponent()
    : mpOwner(nullptr)
{
}

//------------------------------------------------------------------------------------------------------------------------

void GameComponent::SetOwner(GameObject * pOwner)
{
    mpOwner = pOwner;
}

//------------------------------------------------------------------------------------------------------------------------

GameObject & GameComponent::GetGameObject() const
{
    return *mpOwner;
}

